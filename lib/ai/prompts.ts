/**
 * Centralized prompt library for StudyFlow AI.
 *
 * All system prompts live here. Each workflow has a `build*Prompt(ctx)` factory
 * that interpolates student context (name, faculty, course) so the assistant
 * is personalized.
 *
 * DESIGN PRINCIPLES:
 *   1. Persona-first  — every prompt opens with the same StudyFlow AI identity
 *   2. Structure-first — explicit JSON schemas with field descriptions
 *   3. Few-shot       — at least one input→output example per workflow
 *   4. Anti-halluc    — explicit "if you don't know, say so" rules
 *   5. Quality bar    — measurable criteria (e.g., "minimum 5 key points")
 *   6. Edge cases     — instructions for empty/ambiguous input
 *
 * PROMPT CACHING:
 *   OpenAI auto-caches system prompts ≥1024 tokens. Long, well-structured
 *   prompts cost less per call after the first hit. Don't shorten for
 *   "efficiency" — verbosity buys quality AND cache hits.
 */

