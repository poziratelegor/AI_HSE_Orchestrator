// ─────────────────────────────────────────────────────────────────────────────
// PROMPT FACTORIES
// ─────────────────────────────────────────────────────────────────────────────

function withPersona(parts: string[]): string {
  return [PERSONA, "", ...parts].join("\n");
}

function withStudent(ctx: StudentContext | null | undefined, parts: string[]): string {
  const block = renderStudentContextBlock(ctx);
  return block ? [block, ...parts].join("\n") : parts.join("\n");
}

