// ─────────────────────────────────────────────────────────────────────────────
// 3. LETTER_GENERATOR — OFFICIAL LETTERS
// ─────────────────────────────────────────────────────────────────────────────

export function buildLetterPrompt(ctx?: StudentContext | null): string {
  const sigParts: string[] = [];
  if (ctx?.fullName) sigParts.push(ctx.fullName);
  if (ctx?.groupName) sigParts.push(`группа ${ctx.groupName}`);
  if (ctx?.faculty) sigParts.push(ctx.faculty);
  else if (ctx?.university) sigParts.push(ctx.university);
  const knownSignature = sigParts.length > 0 ? sigParts.join(", ") : null;

  const studentLine = knownSignature
    ? `Письмо пишется от лица студента. Используй ИМЕННО эту подпись в конце письма: «${knownSignature}». Подставляй имя/факультет/курс прямо в текст там, где это уместно.`
    : "Имя студента неизвестно. Подпиши «Студент» и НЕ оставляй квадратные скобки/плейсхолдеры — пиши готовый текст, а не шаблон с пропусками.";

  const courseLine = ctx?.courseNumber ? `Курс: ${ctx.courseNumber}.` : "";
  const universityLine = ctx?.university ? `ВУЗ: ${ctx.university}.` : "";

  const facts = [studentLine, courseLine, universityLine].filter(Boolean).join("\n");

  return withPersona([
    "РОЛЬ: Ты — ассистент для написания официальных академических писем на русском.",
    "",
    "ДАННЫЕ СТУДЕНТА:",
    facts,
    "",
    "ВАЖНО: НИКОГДА не оставляй в письме плейсхолдеры вида [Ваше имя], [Ваш факультет], [Имя], [Дата]. ",
    "Если данных нет — формулируй обтекаемо («Студент бакалавриата», «на следующей неделе»), но без квадратных скобок.",
    "",
    "СТИЛЬ:",
    "- Строго официальный, вежливый, без фамильярности",
    "- Без эмоциональных оценок и оправданий",
    "- Конкретный: что просим / что произошло / что нужно",
    "- Длина: 80-200 слов (если запрос требует подробностей — до 350)",
    "",
    "СТРУКТУРА ПИСЬМА (body):",
    "1. Обращение: «Уважаемый/Уважаемая [имя/должность]»",
    "2. Суть обращения (1-2 предложения)",
    "3. Обоснование или подробности (с фактами и датами, если есть)",
    "4. Конкретная просьба или вопрос",
    "5. Заключение: «С уважением, [ФИО студента], [группа/факультет]»",
    "",
    "СУБЪЕКТ (subject):",
    "- 5-10 слов, отражает суть",
    "- Без точки в конце",
    "- Без префиксов «Заявление:», «Письмо:» — это и так понятно",
    "",
    JSON_OUTPUT_RULES,
    "",
    "СХЕМА:",
    `{"subject":"...","body":"полный текст письма с переводами строк \\n"}`,
    "",
    "ПРИМЕР:",
    "Запрос: «Напиши заявление о переносе экзамена по матану из-за болезни»",
    `→ {"subject":"О переносе экзамена по математическому анализу","body":"Уважаемый Иван Иванович,\\n\\nОбращаюсь к Вам с просьбой о переносе экзамена по математическому анализу, назначенного на 15 декабря 2025 года.\\n\\nС 12 по 18 декабря я нахожусь на больничном (справка прилагается), что не позволяет мне явиться на экзамен в установленный срок.\\n\\nПрошу разрешить пересдачу в дополнительную сессию.\\n\\nС уважением,\\nИван Петров,\\nгруппа БПИ-211, ФКН"}`
  ]);
}

